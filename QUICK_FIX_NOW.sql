-- 1. شوف المستخدمين الحاليين
SELECT 
  id,
  email,
  full_name,
  store_id,
  CASE 
    WHEN store_id IS NULL THEN '❌ مش مربوط'
    ELSE '✅ مربوط'
  END as status
FROM user_profiles
ORDER BY created_at DESC;

-- 2. شوف الـ stores الموجودة
SELECT id, name FROM stores;

-- ═══════════════════════════════════════════════════════════════════════════════
-- لو مفيش stores، شغل ده:
-- ═══════════════════════════════════════════════════════════════════════════════

INSERT INTO stores (name, created_at, updated_at)
VALUES ('متجر تيتيانو', NOW(), NOW())
RETURNING id, name;

-- ═══════════════════════════════════════════════════════════════════════════════
-- بعد ما تاخد الـ store_id من فوق، استبدله هنا وشغل:
-- ═══════════════════════════════════════════════════════════════════════════════

-- استبدل STORE_ID_HERE بالـ ID اللي ظهر من الأمر اللي فوق
UPDATE user_profiles 
SET store_id = 'STORE_ID_HERE'
WHERE store_id IS NULL;

-- ═══════════════════════════════════════════════════════════════════════════════
-- تحقق من النجاح:
-- ═══════════════════════════════════════════════════════════════════════════════

SELECT 
  COUNT(*) as total_users,
  COUNT(store_id) as users_with_store,
  COUNT(*) - COUNT(store_id) as users_without_store
FROM user_profiles;

