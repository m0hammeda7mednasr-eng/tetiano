-- الخطوة 1: شوف المستخدمين
SELECT id, email, full_name, store_id FROM user_profiles;

-- الخطوة 2: شوف الـ stores الموجودة
SELECT id, name FROM stores;

-- ═══════════════════════════════════════════════════════════════════════════════
-- لو مفيش stores، أنشئ واحد:
-- ═══════════════════════════════════════════════════════════════════════════════

INSERT INTO stores (name, created_at, updated_at)
VALUES ('متجر تيتيانو', NOW(), NOW())
RETURNING id, name;

-- انسخ الـ ID اللي ظهر

-- ═══════════════════════════════════════════════════════════════════════════════
-- الخطوة 3: اربط كل المستخدمين بالـ store
-- استبدل STORE_ID_HERE بالـ ID من الخطوة 2
-- ═══════════════════════════════════════════════════════════════════════════════

UPDATE user_profiles 
SET store_id = 'STORE_ID_HERE'
WHERE store_id IS NULL;

-- ═══════════════════════════════════════════════════════════════════════════════
-- الخطوة 4: تحقق من النجاح
-- ═══════════════════════════════════════════════════════════════════════════════

SELECT 
  COUNT(*) as total_users,
  COUNT(store_id) as users_with_store,
  COUNT(*) - COUNT(store_id) as users_without_store
FROM user_profiles;

-- لازم users_without_store = 0

