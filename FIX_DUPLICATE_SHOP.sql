-- 1. شوف الـ brands الموجودة بنفس الـ shop domain
SELECT 
  id,
  name,
  shopify_domain,
  store_id,
  is_active,
  connected_at,
  created_at
FROM brands
WHERE shopify_domain = 'qpcich-gi.myshopify.com';

-- ═══════════════════════════════════════════════════════════════════════════════
-- الحل 1: حذف الـ brand القديم (لو مش محتاجه)
-- ═══════════════════════════════════════════════════════════════════════════════

DELETE FROM brands 
WHERE shopify_domain = 'qpcich-gi.myshopify.com';

-- ═══════════════════════════════════════════════════════════════════════════════
-- الحل 2: تحديث الـ brand القديم بدل ما تعمل واحد جديد
-- (استخدم ده لو عايز تحتفظ بالبيانات القديمة)
-- ═══════════════════════════════════════════════════════════════════════════════

-- استبدل STORE_ID بالـ ID الحالي: 6ae0ec4d-9243-4802-b6f6-81b6a74a6f21
UPDATE brands 
SET 
  store_id = '6ae0ec4d-9243-4802-b6f6-81b6a74a6f21',
  updated_at = NOW()
WHERE shopify_domain = 'qpcich-gi.myshopify.com';

