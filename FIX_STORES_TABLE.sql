-- 1. شوف schema الحالي لجدول stores
SELECT column_name, data_type, column_default, is_nullable
FROM information_schema.columns
WHERE table_schema = 'public' 
AND table_name = 'stores'
ORDER BY ordinal_position;

-- ═══════════════════════════════════════════════════════════════════════════════
-- 2. إصلاح الـ id column عشان يولد UUID تلقائياً
-- ═══════════════════════════════════════════════════════════════════════════════

ALTER TABLE stores 
ALTER COLUMN id SET DEFAULT gen_random_uuid();

-- ═══════════════════════════════════════════════════════════════════════════════
-- 3. الآن أنشئ store (هيشتغل بدون مشاكل)
-- ═══════════════════════════════════════════════════════════════════════════════

INSERT INTO stores (name, created_at, updated_at)
VALUES ('متجر تيتيانو', NOW(), NOW())
RETURNING id, name;

-- انسخ الـ ID اللي ظهر

-- ═══════════════════════════════════════════════════════════════════════════════
-- 4. اربط المستخدمين بالـ store (استبدل STORE_ID)
-- ═══════════════════════════════════════════════════════════════════════════════

UPDATE user_profiles 
SET store_id = 'STORE_ID_FROM_STEP_3'
WHERE store_id IS NULL;

-- ═══════════════════════════════════════════════════════════════════════════════
-- 5. احذف الـ brands المكررة
-- ═══════════════════════════════════════════════════════════════════════════════

DELETE FROM brands WHERE shopify_domain IS NOT NULL;

-- ═══════════════════════════════════════════════════════════════════════════════
-- 6. تحقق من النجاح
-- ═══════════════════════════════════════════════════════════════════════════════

-- شوف الـ stores
SELECT id, name, created_at FROM stores;

-- شوف المستخدمين
SELECT 
  COUNT(*) as total_users,
  COUNT(store_id) as users_with_store
FROM user_profiles;

